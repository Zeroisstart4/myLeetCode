class Solution {
    public boolean findWhetherExistsPath(int n, int[][] graph, int start, int target) {

        boolean[] road = new boolean[n + 1];

        road[start] = true;

        for (int i = 0; i < graph.length; i++) {

            if (road[graph[i][0]]) {
                 road[graph[i][1]] = true;
            }
        }

        return road[target];
    }
}
