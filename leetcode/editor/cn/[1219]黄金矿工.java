class Solution {
    public int getMaximumGold(int[][] grid) {

        //è¾¹ç•Œæ¡ä»¶åˆ¤æ–­
        if(grid == null || grid.length == 0) {
            return 0;
        }

        //ä¿å­˜æœ€å¤§è·¯å¾„å€¼
        int res = 0;

        //æŸ¥æ‰¾æœ€å¤§è·¯å¾„å€¼
        for (int i = 0; i < grid.length; i++) {
            for (int j = 0; j < grid[0].length; j++) {
                res = Math.max(res, dfs(grid, i, j));
            }
        }

        return res;
    }

    private int dfs(int[][] grid, int x, int y) {

        //è¾¹ç•Œæ¡ä»¶çš„åˆ¤æ–­
        if(x < 0 || x >= grid.length || y < 0 || y >= grid[0].length || grid[x][y] == 0) {
            return 0;
        }

        //å…ˆæŠŠå½“å‰åæ ‡çš„å€¼ä¿å­˜ä¸‹æ¥ï¼Œæœ€åå†è¿˜åŸ
        int temp = grid[x][y];

        //å½“å‰åæ ‡å·²ç»è®¿é—®è¿‡äº†ï¼Œè¦æŠŠä»–æ ‡è®°ä¸º0ï¼Œé˜²æ­¢å†æ¬¡è®¿é—®
        grid[x][y] = 0;

        //ç„¶åæ²¿ç€å½“å‰åæ ‡çš„ä¸Šä¸‹å·¦å³4ä¸ªæ–¹å‘æŸ¥æ‰¾
        int up = dfs(grid, x - 1, y);
        int down = dfs(grid, x + 1, y);
        int left = dfs(grid, x, y - 1);
        int right = dfs(grid, x, y + 1);

        int max = Math.max(up, Math.max(down, Math.max(left, right)));

        //ç„¶åå†æŠŠå½“å‰ä½ç½®çš„å€¼è¿˜åŸ
        grid[x][y] = temp;

        return grid[x][y] + max;
    }
}
